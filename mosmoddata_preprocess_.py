# Part 2 - from the preprocess nifties (0-255) to npy embbedings
"""
embeddings_final.py
-------------------
Extract DINOv2 CLS embeddings from preprocessed 3D NIfTI files.

Expected input:
  - .nii or .nii.gz files
  - uint8 values in [0, 255]
  - shape (H, W, Z)
  - single-channel grayscale

Output per scan:
  stem.npy   shape (Z, D)   float32
"""

from pathlib import Path

import nibabel as nib
import numpy as np
import torch
from PIL import Image
from transformers import AutoImageProcessor, AutoModel


def slice_to_pil(sl):
    """
    Convert one 2D grayscale slice to a 3-channel RGB PIL image.

    Parameters
    ----------
    sl : np.ndarray
        Array of shape (H, W), values expected in [0, 255].

    Returns
    -------
    PIL.Image.Image
        RGB image.
    """
    sl = np.clip(sl, 0, 255).astype(np.uint8)
    rgb = np.stack([sl, sl, sl], axis=-1)   # (H, W, 3)
    return Image.fromarray(rgb, mode="RGB")


def get_slice_embeddings_cls(volume_3d, processor, model, device, batch_size=16):
    """
    Extract CLS embeddings for all axial slices in a 3D volume.

    Parameters
    ----------
    volume_3d : np.ndarray
        Shape (H, W, Z), values in [0, 255].
    processor : AutoImageProcessor
    model : AutoModel
    device : torch.device
    batch_size : int

    Returns
    -------
    np.ndarray
        Shape (Z, D), float32
    """
    z_dim = volume_3d.shape[2]
    all_embeddings = []

    images = [slice_to_pil(volume_3d[:, :, z]) for z in range(z_dim)]

    for start in range(0, z_dim, batch_size):
        batch_imgs = images[start:start + batch_size]
        inputs = processor(images=batch_imgs, return_tensors="pt")

        inputs = {k: v.to(device) for k, v in inputs.items()}

        with torch.no_grad():
            outputs = model(**inputs)
            cls = outputs.last_hidden_state[:, 0, :]   # (B, D)

        all_embeddings.append(cls.cpu().numpy().astype(np.float32))

    return np.concatenate(all_embeddings, axis=0)   # (Z, D)


def extract_embeddings(input_dir, output_dir, model_dir, batch_size=16):
    """
    Process all NIfTI files in input_dir and save embeddings as .npy files.

    Parameters
    ----------
    input_dir : str or Path
        Folder containing preprocessed .nii / .nii.gz files.
    output_dir : str or Path
        Folder where .npy embeddings will be saved.
    model_dir : str or Path
        Path or HF identifier for DINOv2 model.
    batch_size : int
        Number of slices per forward pass.
    """
    input_dir = Path(input_dir)
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    nii_files = sorted(list(input_dir.glob("*.nii")) + list(input_dir.glob("*.nii.gz")))

    if len(nii_files) == 0:
        print(f"No NIfTI files found in {input_dir}")
        return

    print(f"Found {len(nii_files)} files in {input_dir}")

    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")

    processor = AutoImageProcessor.from_pretrained(model_dir)
    model = AutoModel.from_pretrained(model_dir).to(device)
    model.eval()

    for i, nii_path in enumerate(nii_files, start=1):
        stem = nii_path.name.replace(".nii.gz", "").replace(".nii", "")
        out_path = output_dir / f"{stem}.npy"

        if out_path.exists():
            print(f"[{i}/{len(nii_files)}] Skipping {stem} (exists)")
            continue

        try:
            vol = nib.load(str(nii_path)).get_fdata(dtype=np.float32)

            if vol.ndim != 3:
                raise ValueError(f"Expected 3D volume, got shape {vol.shape}")

            emb = get_slice_embeddings_cls(
                volume_3d=vol,
                processor=processor,
                model=model,
                device=device,
                batch_size=batch_size,
            )

            np.save(str(out_path), emb)
            print(f"[{i}/{len(nii_files)}] Saved {out_path.name}  shape={emb.shape}")

        except Exception as e:
            print(f"[{i}/{len(nii_files)}] ERROR {stem}: {e}")

    print("Done.")


