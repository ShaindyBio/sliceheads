# Part 1: from the raw nifty to 0-255 values

"""
preprocess_mosmed.py
--------------------
Minimal preprocessing for MosMedData NIfTI files.
Saves preprocessed output as .nii.gz, preserving affine and header.

Input volume shape: (512, 512, z)  — x, y, z

Pipeline per scan:
  1. Load NIfTI
  2. HU window [-1000, 400]
  3. Normalize to [0, 255] uint8
  4. Save as .nii.gz  (same affine + header, uint8 data)

Output per scan:
  .nii.gz with shape (512, 512, z)  uint8  values in [0, 255]

# ── What this code does NOT do ────────────────────────────────────────────────
# 1. Does NOT reorient to RAS+ — scan orientation is kept as-is
# 2. Does NOT resample — voxel spacing unchanged, neither in-plane nor z
# 3. Does NOT filter empty slices — all z slices kept, including air/table
# 4. Does NOT resize slices — spatial dims stay at native resolution (512x512)
# 5. Does NOT convert to 3 channels — output is single channel grayscale
# 6. Does NOT apply ImageNet normalization — raw [0, 255] uint8, no mean/std
# 7. Does NOT validate HU range — no check that values are actually in HU
# 8. Does NOT check orientation codes — no warning if scan is not RAS+
# 9. Does NOT handle 4D volumes — will fail if data.ndim != 3
"""

import numpy as np
import nibabel as nib
from pathlib import Path

HU_MIN = -1000
HU_MAX =  400


def preprocess_scan(path, verbose=True):
    """
    Parameters
    ----------
    path    : str or Path  — .nii or .nii.gz file
    verbose : bool

    Returns
    -------
    out_img : Nifti1Image  preprocessed, uint8, shape (512, 512, z)
    meta    : dict         scan metadata
    """
    # 1. Load
    img     = nib.load(str(path))
    header  = img.header
    affine  = img.affine
    data    = img.get_fdata(dtype=np.float32)   # shape (512, 512, z)
    spacing = np.array(header.get_zooms()[:3], dtype=np.float32)

    if verbose:
        print(f"Loaded  : {Path(path).name}")
        print(f"Shape   : {data.shape}  (x, y, z)")
        print(f"Spacing : {spacing} mm")
        print(f"HU range: [{data.min():.1f}, {data.max():.1f}]")

    # 2. HU window [-1000, 400]
    data = np.clip(data, HU_MIN, HU_MAX)

    # 3. Normalize to [0, 255]
    data = (data - HU_MIN) / (HU_MAX - HU_MIN) * 255.0
    data = np.round(data).astype(np.uint8)

    if verbose:
        print(f"Range   : [{data.min()}, {data.max()}]  uint8")

    # 4. Build output NIfTI — same affine, update header dtype
    out_header = header.copy()
    out_header.set_data_dtype(np.uint8)

    out_img = nib.Nifti1Image(data, affine=affine, header=out_header)

    meta = {
        "path":      str(path),
        "shape":     data.shape,
        "spacing":   spacing.tolist(),
        "n_slices":  data.shape[2],
        "hu_window": (HU_MIN, HU_MAX),
    }

    return out_img, meta


def preprocess_dataset(input_dir, output_dir, verbose=False):
    """
    Preprocess all NIfTI files in input_dir and save as .nii.gz.

    Parameters
    ----------
    input_dir  : str — folder containing .nii / .nii.gz files
    output_dir : str — folder to save preprocessed .nii.gz files
    """
    input_dir  = Path(input_dir)
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    nii_files = sorted(
        list(input_dir.glob("*.nii")) +
        list(input_dir.glob("*.nii.gz"))
    )

    print(f"Found {len(nii_files)} files in {input_dir}")

    all_meta = {}

    for i, path in enumerate(nii_files):
        stem     = path.name.replace(".nii.gz", "").replace(".nii", "")
        out_path = output_dir / f"{stem}.nii.gz"

        if out_path.exists():
            print(f"[{i+1}/{len(nii_files)}] Skipping {stem} (exists)")
            continue

        try:
            out_img, meta  = preprocess_scan(path, verbose=verbose)
            all_meta[stem] = meta
            nib.save(out_img, str(out_path))
            print(f"[{i+1}/{len(nii_files)}] Saved {stem}.nii.gz  "
                  f"shape={out_img.shape}")
        except Exception as e:
            print(f"[{i+1}/{len(nii_files)}] ERROR {stem}: {e}")

    # Save metadata
    meta_path = output_dir / "metadata.npy"
    np.save(str(meta_path), all_meta)
    print(f"Metadata saved → {meta_path}")
    print("Done.")


